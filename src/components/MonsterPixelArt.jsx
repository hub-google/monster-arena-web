/**
 * MonsterPixelArt renders custom SVG grids based on stage & type keys.
 * 
 * @param {object} props
 * @param {number} props.stage - The life stage of the monster (1-6).
 * @param {number} props.type - The attribute type (1: Vaccine, 2: Data, 3: Virus).
 * @param {boolean} [props.isDead] - If true, displays a gravestone.
 * @param {boolean} [props.isSick] - If true, renders a skull overlay on top.
 * @param {boolean} [props.isPoop] - If true, renders a poop pile next to it.
 * @param {string} [props.className] - Additional CSS styling.
 */
export default function MonsterPixelArt({ stage, type, isDead = false, isSick = false, isPoop = false, className = '' }) {
  let gridKey = 'egg';

  if (isDead) {
    gridKey = 'tombstone';
  } else if (stage === 1) {
    gridKey = 'egg';
  } else if (stage === 2) {
    gridKey = 'baby';
  } else {
    // Stage 3-6: child_[type], mature_[type], perfect_[type], ultimate_[type]
    const stagePrefix = stage === 3 ? 'child' : stage === 4 ? 'mature' : stage === 5 ? 'perfect' : 'ultimate';
    gridKey = `${stagePrefix}_${type}`;
  }

  const grid = PIXEL_GRIDS[gridKey] || PIXEL_GRIDS.egg;
  const pixelSize = 6; // each pixel is 6px

  return (
    <div className={`relative flex items-center justify-center select-none ${className}`}>
      
      {/* Monster Sprite */}
      <svg 
        width="96" 
        height="96" 
        viewBox="0 0 96 96" 
        className={`${!isDead && stage > 1 ? 'pixel-breathe' : ''}`}
      >
        {grid.map((row, rIdx) => 
          row.map((val, cIdx) => {
            if (val === 1) {
              return (
                <rect 
                  key={`${rIdx}-${cIdx}`} 
                  x={cIdx * pixelSize} 
                  y={rIdx * pixelSize} 
                  width={pixelSize} 
                  height={pixelSize} 
                  fill="#0f380f" // LCD Active Black Color
                />
              );
            }
            return null;
          })
        )}
      </svg>

      {/* Sick Skull Overlay (positioned top right of screen) */}
      {isSick && !isDead && (
        <div className="absolute -top-1 -right-2">
          <svg width="24" height="24" viewBox="0 0 96 96">
            {PIXEL_GRIDS.skull.map((row, rIdx) => 
              row.map((val, cIdx) => {
                if (val === 1) {
                  return (
                    <rect 
                      key={`skull-${rIdx}-${cIdx}`} 
                      x={cIdx * pixelSize} 
                      y={rIdx * pixelSize} 
                      width={pixelSize} 
                      height={pixelSize} 
                      fill="#900" // Red pixel for sick indicator in LCD overlay
                    />
                  );
                }
                return null;
              })
            )}
          </svg>
        </div>
      )}

      {/* Poop Pile next to it */}
      {isPoop && !isDead && (
        <div className="absolute bottom-0 -right-6">
          <svg width="32" height="32" viewBox="0 0 96 96">
            {PIXEL_GRIDS.poop.map((row, rIdx) => 
              row.map((val, cIdx) => {
                if (val === 1) {
                  return (
                    <rect 
                      key={`poop-${rIdx}-${cIdx}`} 
                      x={cIdx * pixelSize} 
                      y={rIdx * pixelSize} 
                      width={pixelSize} 
                      height={pixelSize} 
                      fill="#4b3621" // Poop color overlay
                    />
                  );
                }
                return null;
              })
            )}
          </svg>
        </div>
      )}

    </div>
  );
}
